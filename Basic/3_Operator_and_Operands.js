/* Bitwise Operators

1: Bitwise AND (&): This operator compares each bit of the first operand to the corresponding bit of the 
second operand, and if both bits are 1, the corresponding result bit is set to 1. Otherwise, the 
corresponding result bit is set to 0
2 Bitwise O (|): This operator compares each bit of the first operand to the corresponding bit of the 
second operand, and if at least one of the bits is 1, the corresponding result bit is set to 1
3 Bitwise XO (^): This operator compares each bit of the first operand to the corresponding bit of the 
second operand, and if the bits are different, the corresponding result bit is set to 1. Otherwise, the 
corresponding result bit is set to 0
4 Bitwise NOT (~): This operator inverts all the bits of the operand, effectively swapping 1s for 0s and 0s 
for 1s
5 Left Shift (<<): This operator shifts the bits of the operand to the left by the specified number of places, 
adding zeroes to the right
6 Right Shift (>>): This operator shifts the bits of the operand to the right by the specified number of 
places, discarding the bits shifted out
*/

